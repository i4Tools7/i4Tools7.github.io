var fso = new ActiveXObject("Scripting.FileSystemObject");
var chrome ="C:\\ProgramData\\Microsoft\\Windows\\Start Menu\\Programs\\Google Chrome.lnk" ;
var url = 'https://fsmail.tstartel.com/owa/ --start-maximized --profile-directory="Default" --disk-cache-size=52428800';
if (fso.FileExists(chrome)){
    var objShell = WScript.CreateObject("Shell.Application");
    objShell.ShellExecute(chrome, "--app="+url, "", "", 1);
}
else{ //if chrome doesn''t exists, so launch ie :
    oIE1 = WScript.CreateObject ("InternetExplorer.Application");

    oIE1.Visible = 1;
    oIE1.AddressBar = 0;
    oIE1.StatusBar = 0;
    oIE1.ToolBar = 0;
    oIE1.MenuBar = 0;
    oIE1.Navigate(url);
}

